const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    const decodeSymbol = (numberSymbol) => {
        if (numberSymbol === '**********') return ' ';
        const symbolMorse = numberSymbol.match(/.{2}/g).map(number => {
            if (+number === 10) return '.';
            if (+number === 11) return '-';
        }).join('');

        return MORSE_TABLE[symbolMorse];
    }
    return expr.match(/.{10}/g).map(decodeSymbol).join('');
}

module.exports = {
    decode
}
